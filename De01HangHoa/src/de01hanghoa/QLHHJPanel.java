    private void docTButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_docTButtonActionPerformed
        // TODO add your handling code here:
        try {
            FileInputStream fis= new FileInputStream("D:/dienthoaiText.txt");
            InputStreamReader isr = new InputStreamReader(fis,"UTF-8");
            BufferedReader br = new BufferedReader(isr);
            String line = br.readLine();
            while(line!= null){
                String[] thongTin = line.split("\\$");
                DienThoai dt = new DienThoai(thongTin[0],thongTin[1],thongTin[2],thongTin[3],thongTin[4],Float.parseFloat(thongTin[5]));
                danhSachDT.add(dt);
                line = br.readLine();
            }
//            for(DienThoai temp : danhSachDT){
//                System.out.println(temp);
//            }
            br.close();
            isr.close();
            fis.close();
        } catch (Exception e) {
             e.printStackTrace();
        }
    }//GEN-LAST:event_docTButtonActionPerformed

