    private void themButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themButtonActionPerformed
         if(tenText.getText().trim().isEmpty() || nhaSXText.getText().trim().isEmpty() || giaText.getText().trim().isEmpty()
            || maText.getText().trim().isEmpty() || dLText.getText().trim().isEmpty() || mauText.getText().trim().isEmpty()){
                 thongBao("Khong De Trong Thong Tin");
         }else{
        boolean accept = false;
        String nhaSX = nhaSXText.getText();
        String dl = dLText.getText();
        String mau = mauText.getText();
        String ma = maText.getText();
        String ten = tenText.getText();
        float gb = 0;
        try {
            gb = Float.parseFloat(giaText.getText());
            accept = true;
        } catch (Exception e) {
            e.printStackTrace();
        }
        if(accept){    //String nhaSX, String dungLuongBN, String mauSac, String maHang, String tenHang, float giaBan
          danhSachDT.add(new DienThoai(nhaSX,dl,mau,ma,ten,gb));
          thongBao("Them Thanh Cong");
//          for(DienThoai dt : danhSachDT){
//              System.out.println(dt);
//          }
        }else{
           thongBao("Them Khong Thanh Cong");
        }
         }
    }//GEN-LAST:event_themButtonActionPerformed

    private void luuTButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuTButtonActionPerformed
        
        try {
            FileOutputStream fos = new FileOutputStream("D:/dienthoaiText.txt");
            OutputStreamWriter osw = new OutputStreamWriter(fos,"UTF-8");
            BufferedWriter bw = new BufferedWriter(osw);
            for (int i = 0; i < danhSachDT.size(); i++) {
                DienThoai dt = danhSachDT.get(i);
                              bw.write(dt.getNhaSX()+"$"+dt.getDungLuongBN()+"$"+dt.getMauSac()+"$"
                             +dt.getMaHang()+"$"+dt.getTenHang()+"$"+dt.getGiaBan());
            }
            thongBao("Luu Thanh Cong");
            bw.close();
            osw.close();
            fos.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_luuTButtonActionPerformed

