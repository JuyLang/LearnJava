public class InsertSort extends DaySo implements Sort{

    public InsertSort(int n) {
        super(n);
    }

    @Override
    public void Sort() {
        int n = array.length;
        for (int i = 1; i < n; ++i) {
            int key = array[i];
            int j = i - 1;
            while (j >= 0 && array[j] > key) {
                array[j + 1] = array[j];
                j = j - 1;
            }
            array[j + 1] = key;
        }
    }
    
}
