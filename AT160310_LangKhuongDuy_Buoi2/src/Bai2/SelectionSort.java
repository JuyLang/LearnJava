public class SelectionSort extends DaySo implements Sort{

    public SelectionSort(int n) {
        super(n);
    }

    
    @Override
    public void Sort() {

        int n = array.length;

        for (int i = 0; i < n - 1; i++) {
            int min_idx = i;
            for (int j = i + 1; j < n; j++) {
                if (array[j] < array[min_idx]) {
                    min_idx = j;
                }
            }
            swap(array, i, min_idx);
        }
    }
    private void swap(int[] arr, int a, int b) {
        int temp = arr[a];
        arr[a] = arr[b];
        arr[b] = temp;
    
}
}
